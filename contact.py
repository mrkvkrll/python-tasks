class Contact:

    def __init__(self,name, middle_name, last_name, nickname, company, address, home,
                            phone, work, email):
        self.name = name
        self.middle_name = middle_name
        self.last_name = last_name
        self.nickname = nickname
        self.company = company
        self.address = address
        self.home = home
        self.phone = phone
        self.work = work
        self.email = email